const mongoose = require('mongoose');

const doctorSchema=mongoose.Schema({
    firstName:{
        type:String,
        required:true,
    },
    lastName:{
        type:String,
        required:true,
    },
    email:{
        type:String,
        required:true,
        unique:true,
    },
    password:{
        type:String,
        required:true,
    },
    imagePath:String
})

module.exports=mongoose.model('Doctor',doctorSchema)